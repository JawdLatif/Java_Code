package com.constractor;

public class Getter_Setter {
	
	private String prodctName;
	private int prodctID;
	private String productExpirationDate;
	
	
	
//	getter and setter
	public String getProdctName() {
		return prodctName;
	}
	public void setProdctName(String prodctName) {
		this.prodctName = prodctName;
	}
	public int getProdctID() {
		return prodctID;
	}
	public void setProdctID(int prodctID) {
		this.prodctID = prodctID;
	}
	public String getProductExpirationDate() {
		return productExpirationDate;
	}
	public void setProductExpirationDate(String productExpirationDate) {
		this.productExpirationDate = productExpirationDate;
	}
	

	
	

}
